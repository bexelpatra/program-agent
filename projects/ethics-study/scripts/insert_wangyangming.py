    # 기존에 있는 relation 확인
    existing_relations = []
    try:
        resp = client.search(index=INDEX_RELATIONS, body={
            "query": {
                "bool": {
                    "should": [
                        {"term": {"to_thinker": "wangyangming"}},
                        {"term": {"from_thinker": "wangyangming"}}
                    ]
                }
            },
            "size": 20
        })
        for hit in resp['hits']['hits']:
            s = hit['_source']
            existing_relations.append((s['from_thinker'], s['to_thinker'], s['type']))
        print(f"기존 관계 {len(existing_relations)}건 확인:")
        for r in existing_relations:
            print(f"  {r[0]} -> {r[1]} ({r[2]})")
    except Exception as e:
        print(f"[relations] 조회 오류: {e}")

    count_skipped = 0
    count_added = 0

    for rel in relations_to_add:
        key = (rel['from_thinker'], rel['to_thinker'], rel['type'])
        if key in existing_relations:
            print(f"[relation] SKIP (already exists): {rel['from_thinker']} -> {rel['to_thinker']} ({rel['type']})")
