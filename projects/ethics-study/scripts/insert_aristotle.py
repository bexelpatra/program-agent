def verify_insertion(client):
    """입력된 데이터를 간단히 검증한다."""
    from src.es_client import search_documents

    print("\n=== 검증 ===")

    # thinker 확인
    thinker = client.get(index=INDEX_THINKERS, id="aristotle")
    print(f"[OK] thinker: {thinker['_source']['name']} ({thinker['_source']['name_en']})")

    # works 건수 확인
    works = search_documents(client, INDEX_WORKS, {"term": {"thinker_id": "aristotle"}}, size=20)
    print(f"[OK] works: {works['hits']['total']['value']}건")

    # claims 건수 확인
    claims = search_documents(client, INDEX_CLAIMS, {"term": {"thinker_id": "aristotle"}}, size=20)
    print(f"[OK] claims: {claims['hits']['total']['value']}건")

    # keywords 건수 확인
    keywords = search_documents(client, INDEX_KEYWORDS, {"term": {"thinker_id": "aristotle"}}, size=20)
    print(f"[OK] keywords: {keywords['hits']['total']['value']}건")

    # relations 건수 확인
    relations = search_documents(
        client, INDEX_RELATIONS,
        {"bool": {"should": [
            {"term": {"from_thinker": "aristotle"}},
            {"term": {"to_thinker": "aristotle"}}
        ]}},
        size=20
    )
    print(f"[OK] relations (aristotle 관련 전체): {relations['hits']['total']['value']}건")

    # argument/counterpoint 검증
    print("\n[argument/counterpoint 확인]")
    for hit in claims['hits']['hits']:
        src = hit['_source']
        has_arg = bool(src.get('argument', '').strip())
        has_cp = bool(src.get('counterpoint', '').strip())
        print(f"  {src['id']}: argument={'OK' if has_arg else 'MISSING'}, counterpoint={'OK' if has_cp else 'EMPTY'}")


